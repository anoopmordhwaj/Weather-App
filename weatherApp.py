import requests

def weather(api_key, city):
    # this is the url which gonna hit ....and takes city and api_key as arguments.
    res = requests.get(f'https://api.openweathermap.org/data/2.5/weather?q={city}&appid={api_key}&units=metric')
    json_data = res.json()

    if json_data['cod'] == 200:
        # status code 200 is a succes code ..it present at last of json data
        print(f"Weather in {city}")
        print(f"Temperature: {json_data['main']['temp']}Â°C")
